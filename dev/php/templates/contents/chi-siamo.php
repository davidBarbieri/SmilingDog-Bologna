            <header class="title-wrapper">
                <h2>Cristina Collina</h2>
            </header>

            <p>Da sempre appassionata del mondo canino, mi sono affacciata alla cinofilia nel 2007 quando arrivò a casa Dana, una cucciola di Border Collie dal carattere un po’ difficile. Ho iniziato con lei un percorso di educazione di base e successivamente ho iniziato a praticare l’Agility.</p>
            <p>Interessata sempre più ad approfondire il tema del rapporto che si crea tra cane e proprietario,a  settembre 2009 ho deciso di intraprendere il corso per diventare Istruttore Cinofilo presso il Centro Studi del Cane Italia e ho conseguito il diploma a Gennaio 2011.</p>
            <p>Ho poi iniziato a frequentare stage di Obedience diretti da Luca Rossi  e,attratta sempre più da questo sport, ho iniziato a praticarlo con continuità sotto la guida di Daniela Scanelli che ancora mi segue negli allenamenti.</p>
            <p>Nel  2011 si è unita alla famiglia Grace, un’altra cucciola di Border Collie, con cui oggi gareggio in Classe 3 ENCI di Obediencee che nell’anno 2016 è stata proclamata Campionessa Italiana di Obedience ENCI.</p>
            <p>Nel 2013 ho seguito il corso APICC  per diventare Operatore Cani da Supporto a Disabili.</p>
            <p>Nel 2014 ho partecipato al Corso Formativo Csen per Tecnici di Rally Obedience con docente Ilenia Sinatò.</p>
            <p>Nel 2015, obbligata a pensionare Dana dall’attività agonistica dell’agility, ho iniziato a praticare questo sport con Mojito, Pastore delle Shetland,  con cui in poco tempo ho raggiunto il 3° grado ENCI, classe in cui tutt’oggi gareggiamo.</p>
            <p>Sempre nel 2015 ho seguito il Corso per Istruttori di Obedience con docenti Daniela Scanelli, Silvia Cacciatore e Gianfranco Giraudi.</p>
            <p>La mia preparazione continua con la partecipazione a diversi stage di Agility e Obedience tenuti da Istruttori di fama nazionale e internazionale.</p>
            <p>Al Centro Cinofilo Smiling Dog Bologna mi occupo dei corsi di educazione di base e dei corsi di Agility, Obedience e Rally Obedience e della preparazione dei cani da supporto a disabili.</p>
