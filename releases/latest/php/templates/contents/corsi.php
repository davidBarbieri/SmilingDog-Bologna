        </header>

        <p> I Tricks sono divertenti esercizi che possono essere insegnati a cani di qualsiasi età. Questo percorso è adatto a tutti i proprietari che hanno già un buon rapporto col proprio cane e vogliono svilupparne ulteriormente le competenze, tenerne allenata la mente e le capacità di apprendimento e soprattutto investire in attività piacevoli per entrambi il tempo trascorso insieme.</p>
    </section>

