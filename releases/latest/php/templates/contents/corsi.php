        <header class="title-wrapper">
            <h3>Rally Obedience</h3>
        </header>

        <p> La Rally Obedience è una disciplina sportiva del circuito C.S.E.N. che consiste in un percorso a tappe (cartelli) dove ogni tappa rappresenta un esercizio di obbedienza che il conduttore deve far eseguire al cane. La difficoltà degli esercizi e del percorso aumenta mano a mano che si sale di grado.</p>
        <p>La gara non è a tempo in quanto ciò che conta per il giudizio e la classifica finale è il punteggio ottenuto in base alla precisione con cui si sono svolti i vari esercizi indicati nei cartelli. Il tempo è una variabile significativa solo ed esclusivamente in caso di parità di punteggio di due o più binomi: in questo caso il binomio che ha svolto il percorso nel tempo inferiore sarà vincente rispetto agli altri.</p>
    </section>

