        <header class="title-wrapper">
            <h3>Obedience</h3>
        </header>

        <p>L’Obedience è una disciplina sportiva riconosciuta dall’E.N.C.I. (Ente Nazionale della Cinofilia Italiana) e dalla F.C.I. (FederationCynologique International) aperta a tutti i cani che ha come fine l’insegnamento al cane di un comportamento  controllato e collaborativo, favorendo così la sua buona integrazione nella società. Questa disciplina implica una buona armonia tra il cane e il suo conduttore che porta ad una intesa perfetta tra i due anche quando il cane si trova distante dal conduttore.</p>
        <p>Nelle Prove di Obedience sono previste cinque classi (Predebuttanti, Debuttanti, Classe1 , Classe 2 e Classe 3) dove la difficoltà degli esercizi aumenta man mano che si avanza di grado.</p>
    </section>

