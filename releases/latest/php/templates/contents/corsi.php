            <h3>Puppy Class</h3>
        </header>

        <p> La Puppy Class è un corso di socializzazione ed educazione dedicato ai cuccioli di età compresa tra i 2 e i 6 mesi. Questo corso ha lo scopo di fornire ai proprietari le informazioni necessarie per gestire correttamente il cucciolo e per instaurare con lui un forte legame basato sul rispetto e la collaborazione.</p>
        <p>Durante gli incontri il cucciolo socializzerà con cani e persone, apprenderà i primi comandi di obbedienza di base e  inizierà ad instaurare un rapporto affettivo col proprietario che imparerà a gestire al meglio il suo amico a quattro zampe grazie ai consigli forniti dall’istruttore.</p>
    </section>

