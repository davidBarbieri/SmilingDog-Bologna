        <header class="title-wrapper">
            <h3>CAE-1 ENCI</h3>
        </header>

        <p>Il CAE-1 è un Test di Controllo dell’Affidabilità e dell’Equilibrio Psichico dei cani nonché delle capacità di controllo da parte del conduttore.</p>
        <p>Questo test  mira a certificare un cane socialmente affidabile e senza problematiche di comportamento prendendo in considerazione il binomio cane-conduttore nella vita quotidiana.</p>
        <p><strong>Le prove che il binomio deve affrontare sono dieci:</strong>
            <ol>
                <li>Passaggio di operatori a diverse andature</li>
                <li>Passaggio di un operatore in bicicletta nel campo visivo del cane</li>
                <li>Passaggio di un cane di sesso opposto</li>
                <li>Un operatore spinge un passeggino nel campo visivo del cane</li>
                <li>Un operatore corre(jogger)nel campo visivo del cane passandogli vicino</li>
                <li>Dimostrare di essere in grado di manipolare ed accudire il proprio cane</li>
                <li>Passaggio di operatori che incrociano e sfiorano il cane</li>
                <li>Un operatore seduto chiude il giornale ed a passo veloce incrocia la coppia</li>
                <li>Un operatore si avvicina, rivolge parole al cane e stringe la mano al conduttore</li>
                <li>Un operatore si avvicina alla coppia con un ombrello chiuso picchiettando sul terreno lo apre e si allontana</li>
            </ol>
        </p>
    </section>

