        <header class="title-wrapper">
            <h3>Problemi comportamentali</h3>
        </header>

        <p> A seguito di errori nella gestione, eventi traumatici o per problemi verificatisi nei primi due mesi del cane, fondamentali per uno sviluppo equilibrato del soggetto, il cane potrebbe manifestare problemi di comportamento quali ansia, aggressività o fobie.</p>
        <p>Per correggere ed arginare le problematiche sviluppate dal cane è fondamentale intraprendere un percorso di rieducazione in base ai tratti specifici del soggetto.</p>
    </section>

