        <p> La persona disabile ha spesso difficoltà a svolgere attività come per esempio aprire o chiudere una porta o un cassetto, recuperare oggetti caduti a terra, prendere il telefono da un mobile, accendere e spegnere la luce.</p>
        <p>Il cane da assistenza, adeguatamente preparato, può migliorare la vita della persona disabile aiutandola a svolgere attività di routine come quelle sopra elencate che da solo non riuscirebbe a svolgere o che svolgerebbe con enorme difficoltà,  permettendogli quindi di essere autosufficiente.</p>
